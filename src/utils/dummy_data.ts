// Helper functions
export const getRandomStudents = (count: number): StudentRecord[] => {
  const shuffled = [...studentRecords].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getStudentsByCourse = (courseName: string): StudentRecord[] => {
  return studentRecords.filter(student => student.course === courseName);
};

export const getStudentById = (id: string): StudentRecord | undefined => {
  return studentRecords.find(student => student.id === id);
};

export const getCourseAverage = (courseName: string): number => {
  const students = getStudentsByCourse(courseName);
  const sum = students.reduce((acc, student) => acc + student.grade, 0);
  return students.length > 0 ? Math.round(sum / students.length) : 0;
};

export const getTopPerformers = (count: number = 5): StudentRecord[] => {
  return [...studentRecords]
    .sort((a, b) => b.grade - a.grade)
    .slice(0, count);
}; 